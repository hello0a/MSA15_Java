package day01;

public class Ex01_Print {
	// main : ctrl + space
	// 프로그램 시작되는 메소드
	public static void main(String[] args) {
		// 기본 출력 메소드
		// - print(), println() 메소드 호출하여 출력
		// - System.out 출력 객체 사용
		// sysout : ctrl + space
		System.out.print("안녕하세요");
		
		// : 지정한 문자열을 출력한 후, 한 줄 엔터
		System.out.println("안녕하세요 출력 후 줄 바꿈");
		// 바로 엔터 : shift + enter
		System.out.println("자바 첫 수업");
		System.out.println();
		System.out.println("이어지는 문자열...");
	}
}
